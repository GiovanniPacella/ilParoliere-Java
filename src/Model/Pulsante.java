package Model;

public class Pulsante {
    private int x;
    private int y;
    private char carattere;

    public char getCarattere() {
        return carattere;
    }

    public void setCarattere(char carattere) {
        this.carattere = carattere;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
